package org.talend.dataprep.transformation.api.action.metadata;

public class Item {

    String name;

    Type type;

    String category;

    String value;

    public String getName() {
        return name;
    }

    public Type getType() {
        return type;
    }

    public String getCategory() {
        return category;
    }

    public String getValue() {
        return value;
    }
}
